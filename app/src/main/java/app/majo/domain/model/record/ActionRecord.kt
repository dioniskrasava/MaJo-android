package app.majo.domain.model.record

/**
 * Модель записи о выполнении активности.
 *
 * Это ключевая **доменная модель** для трекинга. Она представляет собой один
 * зафиксированный факт выполнения, связанный с конкретным [app.majo.domain.model.action.Action]
 * через поле [activityId].
 *
 * Примеры:
 * - "Пробежал 3 км"
 * - "Медитировал 20 минут"
 * - "Сделал 50 отжиманий"
 *
 * totalPoints — кешированное значение,
 * чтобы быстро показывать статистику.
 *
 * @property id Уникальный идентификатор данной записи о выполнении (Primary Key).
 * @property activityId Идентификатор связанной активности [app.majo.domain.model.action.Action].
 * Это внешний ключ, указывающий, к какой активности относится запись.
 * @property value Фактическое значение выполнения (например, 3.0, 20.0).
 * @property timestamp Временная метка (в миллисекундах), когда запись была создана или завершена.
 * @property totalPoints Общее количество очков, полученных за это выполнение.
 * Кешированное (предварительно рассчитанное) значение.
 */
data class ActionRecord(
    val id: Long,
    val activityId: Long,
    val value: Double,
    val timestamp: Long,
    val totalPoints: Double
)