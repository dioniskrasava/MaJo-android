package app.majo.domain.model.action

/**
 * Тип отслеживаемой активности.
 *
 * Определяет основной принцип измерения выполнения активности и
 * влияет на логику валидации ввода и расчета очков.
 *
 * DISTANCE — расстояние (км, метры и т.п.)
 * TIME — время (минуты, часы)
 * COUNT — количество повторений или единиц
 * BINARY — бинарная активность (сделано / не сделано)
 */
enum class ActionType {
    /**
     * Активность, результат которой измеряется **расстоянием** (например, бег, езда на велосипеде).
     */
    DISTANCE,

    /**
     * Активность, результат которой измеряется **временем** (например, медитация, учеба).
     */
    TIME,

    /**
     * Активность, результат которой измеряется **количеством повторений, объемом или штуками**
     * (например, отжимания, прочитанные страницы).
     */
    COUNT,

    /**
     * **Бинарная** активность — фиксируется только факт выполнения (Да/Нет, Сделано/Не сделано).
     * В этом случае поле `value` в [ActionRecord] обычно равно 1.
     */
    BINARY
}