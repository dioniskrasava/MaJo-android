package app.majo.domain.model.action

/**
 * Единицы измерения для активности.
 *
 * Определяет, в каких единицах будет отслеживаться выполнение активности
 * (например, километры, минуты, повторения).
 *
 * Для бинарных типов (ActionType.BINARY) обычно используется NONE.
 */
enum class UnitType {
    /**
     * Отсутствие единицы измерения. Используется для бинарных активностей
     * или в случаях, когда единица измерения не требуется.
     */
    NONE,

    /**
     * Единица измерения: Километр.
     */
    KM,

    /**
     * Единица измерения: Метр.
     */
    METER,

    /**
     * Единица измерения: Минута.
     */
    MINUTE,

    /**
     * Единица измерения: Час.
     */
    HOUR,

    /**
     * Единица измерения: Повторение (для подсчета количества действий).
     */
    REPETITION
}