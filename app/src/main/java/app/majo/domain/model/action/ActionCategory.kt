package app.majo.domain.model.action

/**
 * Категории активности, используемые для
 * аналитики и удобной группировки в интерфейсе.
 *
 * Использование Enum вместо просто строк обеспечивает:
 * 1. **Типобезопасность (Type Safety)**: Невозможно использовать категорию, не определенную здесь.
 * 2. **Автодополнение**: Упрощает написание кода.
 * 3. **Чистоту кода**: Повышает читаемость бизнес-логики.
 */
enum class ActionCategory {
    /**
     * Категория для физических нагрузок и спорта.
     */
    FITNESS,

    /**
     * Категория для задач, связанных с работой, учебой или личными проектами.
     */
    PRODUCTIVITY,

    /**
     * Категория для действий, связанных с заботой о здоровье и благополучием
     * (например, сон, медитация, прием лекарств).
     */
    HEALTH,

    /**
     * Категория для обучающих активностей (например, чтение, курсы, лекции).
     */
    EDUCATION,

    /**
     * Категория по умолчанию или для активностей, не попадающих в основные группы.
     */
    OTHER
}