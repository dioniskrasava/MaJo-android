package app.majo.ui.screens.action_list

import app.majo.domain.model.action.Action

/**
 * Состояние экрана списка активностей.
 *
 * Состояние — это "истина" UI: он рисуется строго на основании [ActionListState].
 * UI не хранит данные сам — вся логика и данные инкапсулированы и передаются
 * из [ActionListViewModel].
 *
 * @property actions Список всех доменных моделей [Action], которые должны быть отображены.
 * @property isLoading Флаг, указывающий, идёт ли в данный момент загрузка данных
 * из репозитория (например, при первом запуске).
 * @property error Сообщение об ошибке (например, "Не удалось подключиться к БД").
 * Если null, ошибки нет.
 */
data class ActionListState(

    /**
     * Список всех активностей, полученных из репозитория.
     * Если список пуст, UI может отобразить индикатор загрузки, заглушку
     * ("Добавьте первую активность") или сообщение об ошибке.
     */
    val actions: List<Action> = emptyList(),

    /**
     * Показывает, идёт ли загрузка данных.
     * Используется UI для отображения индикатора прогресса (например, CircularProgressIndicator).
     */
    val isLoading: Boolean = false,

    /**
     * Сообщение об ошибке, если что-то пошло не так при получении данных.
     * Используется UI для отображения уведомления или диалога.
     */
    val error: String? = null
)