package app.majo.ui.screens.action_list

/**
 * Определяет все возможные **события (Intents)**, которые могут быть отправлены
 * от пользовательского интерфейса (View/Composable) к ViewModel.
 *
 * Это ключевой элемент архитектуры **MVI (Model-View-Intent)**,
 * где UI посылает "намерения" (Intents), а ViewModel реагирует на них.
 *
 * Почему sealed interface?
 * - Позволяет чётко перечислить **ВСЕ** возможные события, связанные с экраном.
 * - При обработке события в ViewModel (используя выражение `when (event)`),
 * компилятор Kotlin гарантирует, что учтены все подклассы (`sealed`), что повышает **типобезопасность**
 * и исключает ошибки, связанные с необработанными случаями.
 */
sealed interface ActionListEvent {

    /**
     * Событие: Пользователь нажал на карточку активности.
     *
     * Это событие содержит данные, необходимые для дальнейшего действия (навигации).
     *
     * @property id Уникальный ID активности, который ViewModel использует для:
     * 1. Загрузки деталей активности.
     * 2. Навигации на экран редактирования или просмотра.
     */
    data class OnActionClick(val id: Long) : ActionListEvent

    /**
     * Событие: Пользователь нажал на кнопку добавления (+).
     *
     * Используется для запуска навигации на экран создания новой активности.
     */
    object OnAddClick : ActionListEvent
}